#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			k;
	unsigned char	*p;

	k = 0;
	p = (unsigned char *)s;
	while (n--)
	{
		*(p + k) = 0;
		k++;
	}
}
